// Pre-bundles the Express API into a single dependency-free ESM file for Vercel's
// serverless function builder. Vercel transpiles /api/*.ts file-by-file rather than
// bundling, which breaks Node's native ESM resolver on our extensionless workspace
// imports (apps/api/src/*, @compass/shared). Bundling here sidesteps that entirely —
// only external npm packages (resolved from node_modules at runtime) remain unbundled.
import { build } from 'esbuild';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const root = dirname(dirname(fileURLToPath(import.meta.url)));

await build({
  entryPoints: [resolve(root, 'apps/api/src/serverless.ts')],
  outfile: resolve(root, 'api/index.mjs'),
  bundle: true,
  platform: 'node',
  format: 'esm',
  target: 'node20',
  packages: 'external',
  logLevel: 'info',
  // A checked-in copy of api/index.mjs must exist so Vercel detects the
  // serverless function when it validates vercel.json's `functions` glob —
  // that check runs against the cloned source tree, before the build command.
  // This banner marks the committed artifact; `vercel build` regenerates it
  // fresh from apps/api/src on every deploy, so source is the source of truth.
  banner: { js: '// GENERATED by scripts/bundle-api.mjs — do not edit. Regenerated on every `vercel build`.' },
});
